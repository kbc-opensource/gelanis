    """
    Verify whether current token is a valid decimal token (which contains dot).
    Returns true if the character that follows the token is not a digit or letter or underscore.

    For example:
    For char stream "2.3", "2." is not a valid decimal token, because it is followed by digit '3'.
    For char stream "2.3_", "2.3" is not a valid decimal token, because it is followed by '_'.
    For char stream "2.3W", "2.3" is not a valid decimal token, because it is followed by 'W'.
    For char stream "12.0D 34.E2+0.12 "  12.0D is a valid decimal token because it is folllowed
    by a space. 34.E2 is a valid decimal token because it is followed by symbol '+'
    which is not a digit or letter or underscore.
    """

    def isValidDecimal(self):
        nextChar = self._input.LA(1)
        return not ("A" <= nextChar <= "Z" or "0" <= nextChar <= "9" or nextChar == "_")

    def sempred(self, localctx: RuleContext, ruleIndex: int, predIndex: int):
        if self._predicates is None:
            preds = dict()
            preds[228] = self.DECIMAL_VALUE_sempred
            preds[229] = self.SCIENTIFIC_DECIMAL_VALUE_sempred
            preds[230] = self.DOUBLE_LITERAL_sempred
            preds[231] = self.BIGDECIMAL_LITERAL_sempred
            self._predicates = preds
        pred = self._predicates.get(ruleIndex, None)
        if pred is not None:
            return pred(localctx, predIndex)
        else:
            raise Exception("No registered predicate for:" + str(ruleIndex))

    def DECIMAL_VALUE_sempred(self, localctx: RuleContext, predIndex: int):
        if predIndex == 0:
            return self.isValidDecimal()

    def SCIENTIFIC_DECIMAL_VALUE_sempred(self, localctx: RuleContext, predIndex: int):
        if predIndex == 1:
            return self.isValidDecimal()

    def DOUBLE_LITERAL_sempred(self, localctx: RuleContext, predIndex: int):
        if predIndex == 2:
            return self.isValidDecimal()

    def BIGDECIMAL_LITERAL_sempred(self, localctx: RuleContext, predIndex: int):
        if predIndex == 3:
            return self.isValidDecimal()
